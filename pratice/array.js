let a = 4
const b = 5
var c = 'Hello'

const array = [a,b,c]
console.log(array); // [4, 5, 'Hello'] as array is a collection of elements

const arr = [4,8,'sandeep']
console.log(arr); // [4, 8, 'sandeep'] as array is a collection of elements
console.log(arr[0]); // 4 as array index starts from 0
console.log(arr[1]); // 8 as array index starts from 0
console.log(arr[2]); // 'sandeep' as array index starts from 0
console.log(arr[3]); // undefined as array index starts from 0 and there is no element at index 3
console.log(arr.length); // 3 as length of array is 3